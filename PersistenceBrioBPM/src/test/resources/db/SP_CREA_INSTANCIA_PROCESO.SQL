--
DECLARE @CH_TIPO_EXCEPCION				VARCHAR		(20)			-- TIPO DE EXCEPCION 'OK', 'ERROR', 'AVISO'
DECLARE @CH_MENSAJE						VARCHAR		(2000)			-- MENSAJE
DECLARE @CH_CVE_INSTANCIA				VARCHAR		(40)			-- CLAVE DE INSTANCIA

EXEC	SP_CREA_INSTANCIA_PROCESO
			'Gabriela.Arias',						-- USUARIO CREADOR
			'BRIOMAX',								-- ENTIDAD
			'BRIOMAX-CENTRAL',						-- LOCALIDAD
			'ES-MX',								-- IDIOMA
			'INCIDENCIA-SMI',						-- CLAVE DEL PROCESO
			1.00,									-- VERSIÓN
			'NUEVA INCIDENCIA',						-- CONCEPTO
			'USUARIO',								-- ORIGEN DE CREACIÓN DE INSTANCIA
			'CLIENTE',								-- ROL QUE CREA INSTANCIA
			@CH_CVE_INSTANCIA			OUT,		-- CLAVE DE INSTANCIA
			@CH_CVE_NODO_ACTIVIDAD		OUT,		-- CLAVE DEL NODO DE ACTIVIDAD CREADO
			@I_ID_NODO_ACTIVIDAD		OUT,		-- ID DEL NODO ACTIVIDAD CREADO
			@I_SECUENCIA_NODO_ACTIVIDAD	OUT,		-- SECUENCIA DEL NODO DE ACTIVIDAD CREADO
			@CH_TIPO_EXCEPCION			OUT,		-- TIPO DE EXCEPCIÓN 'OK', 'ERROR', 'AVISO'
			@CH_MENSAJE					OUT			-- MENSAJE Y SUGERENCIA

SELECT '@CH_CVE_INSTANCIA=', @CH_CVE_INSTANCIA
SELECT '@CH_TIPO_EXCEPCION=', @CH_TIPO_EXCEPCION
SELECT '@CH_MENSAJE=', @CH_MENSAJE

SELECT * FROM IN_NODO_PROCESO		WHERE CVE_INSTANCIA = @CH_CVE_INSTANCIA
SELECT * FROM IN_VARIABLE_PROCESO	WHERE CVE_INSTANCIA = @CH_CVE_INSTANCIA
SELECT * FROM IN_TAREA_NODO			WHERE CVE_INSTANCIA = @CH_CVE_INSTANCIA
SELECT * FROM IN_DOCUMENTO_NODO		WHERE CVE_INSTANCIA = @CH_CVE_INSTANCIA
